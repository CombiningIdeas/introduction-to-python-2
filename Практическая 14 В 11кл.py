import random

pi = 3.141828# при N = 100000000

N = 1000000
M = 0

for i in range(N):
    x = random.random()
    y = random.random()
    if x*x+y*y <= 1:
        M += 1

Pi = 4*M/N

print("PI =", pi)

R = 5

S = pi * (R*R)

print("S =", S)

